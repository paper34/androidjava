package 반복문;

public class 연습문제3 {

	public static void main(String[] args) {
		//별찍기
		for (int i = 1; i < 6; i++) {
			for (int j = 0; j < i; j++) {
				System.out.print("*");
			}
			System.out.println("");
		}

		
		for (int a = 0; a < 4; a++) {
			for (int b = a; b < 4; b++) {
				System.out.print("*");
			}
			System.out.println("");
		}
		
		
	}

}
	